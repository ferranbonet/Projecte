    private void reiniciarFormulari() {
        textFieldNom.setText("");
        textFieldGenere.setText("");
        textFieldDescripcio.setText("");
        textFieldNumCapitols.setText("");
        textFieldDuracioCapitols.setText("");
        textFieldAnyEmissio.setText("");
        textFieldNota.setText("");
        checkBoxAcabat.setSelected(false);
        buttonEliminar.setEnabled(false);
        buttonModificar.setEnabled(false);
        metodeGUI.carregarTaula(new String[]{"Fila", "Nom", "Gènere", "Descripció", "Número de capítols", "Duració capítols: ", "Any d'emissió", "Nota", "Acabat"}, transformarDades(Projecte.getArray()), tableDades);
    }

    private boolean dadesCorrectes() {
        try {
            if (textFieldNom.getText().trim().equals("")) {
                throw new StringIndexOutOfBoundsException();
            }
            if (textFieldGenere.getText().trim().equals("")) {
                throw new StringIndexOutOfBoundsException();
            }
            if (textFieldDescripcio.getText().trim().equals("")) {
                throw new StringIndexOutOfBoundsException();
            }
            Integer.valueOf(textFieldNumCapitols.getText());
            Integer.valueOf(textFieldDuracioCapitols.getText());
            Integer.valueOf(textFieldAnyEmissio.getText());
            Double.valueOf(textFieldNota.getText());
        } catch (StringIndexOutOfBoundsException | NumberFormatException e) {
            return false;
        }
        return true;
    }

