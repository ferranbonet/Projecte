    public static Object[][] transformarDades(Serie[] dades) {
        Object[][] resultat = new Object[dades.length][9];
        int omplert = 0;
        for (int i = 0; i < dades.length; i++) {
            if (dades[i].isOmplert()) {
                resultat[omplert][0] = i;
                resultat[omplert][1] = dades[i].getNom();
                resultat[omplert][2] = dades[i].getGenere();
                resultat[omplert][3] = dades[i].getDescripcio();
                resultat[omplert][4] = dades[i].getNumCapitols();
                resultat[omplert][5] = dades[i].getDuracioCapitols();
                resultat[omplert][6] = dades[i].getAnyEmissio();
                resultat[omplert][7] = dades[i].getNota();
                resultat[omplert][8] = dades[i].isAcabat();
                omplert++;
            }
        }
        return Arrays.copyOf(resultat, omplert);
    }

